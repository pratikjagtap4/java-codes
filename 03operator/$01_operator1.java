class $01_operator1
{
	public static void main(String[] args)
	{
		int result;

		result = 10 + 20;
		System.out.println(result);

		result = 10 - 5;
		System.out.println(result);

		result = 10 * 2;
		System.out.println(result);

		result = 11 / 2;
		System.out.println(result);

		result = 11 % 2;
		System.out.println(result);

	}
}