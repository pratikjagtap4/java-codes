class $13_switch1
{
	public static void main(String[] args)
	{
		int a = 2;

		switch(a)
		{
			case 1 : System.out.println("inside case 1");
					 break;
					
			case 2 : System.out.println("inside case 2");
					 break;

			case 3 : System.out.println("inside case 3");
					 break;
				
			default : System.out.println("inside defailt case");
					  break;
			
		}
	}
			
}