class $20_switch10
{
	public static void main(String[] args)
	{
		char var =100;

		switch(var)
		{
			case 'a' : System.out.println("a case is matched");
						break;
			
			case 'b' : System.out.println("b case is matched");
					break;

			default : System.out.println("default case is matched");

		}
	}
}