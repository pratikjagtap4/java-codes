class $24_switch14
{
	public static void main(String[] args)
	{
		int var = 10;
		switch(var)
		{
			default :
				System.out.println("default case is match");
				break;


			case 20:
				System.out.println("case 20 is match");
				break;

			case 10:
				System.out.println("case 10 is match");
			break;
		}
	}
}

// order of case does not matter